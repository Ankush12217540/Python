# COMPARISION OPERATOR

# Equal
print(2==2)
print(1==0)

# Note that '==' is a comparision operator, while '=' is an assignment operator

# Not Equal
print(2!=2)
print(2!=1)

# Greater Than
print(2>1)
print(2>4)

# Less Than
print(2<4)
print(2<1)

# Greater than or Equal to 
print(2>=2)
print(2>=1)

# Less Than or Equal to 
print(2<=2)
print(2<=4)
