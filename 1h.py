# SETS AND BOOLEANS

#sets
x=set()
# we add to sets with the add() method
x.add(1)
print(x)
# adding a different element 
x.add(2)
print(x) 
#adding the same element
x.add(1)
print(x)
# create a list with repeats
list1=[1,1,2,2,3,4,5,6,1,1]
#cast as set to get unique values
print(set(list1))

